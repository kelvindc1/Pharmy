    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            String idString = String.valueOf(tblContrato.getValueAt(tblContrato.getSelectedRow(), 0));

            id = Integer.parseInt(idString);

            ContratoDAO apDAO = new ContratoDAO();

            if (JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?") == JOptionPane.OK_OPTION) {
                if (apDAO.excluir(id)) {
                    JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
                    new ContratoDAO().popularTabela(tblContrato, "");
                } else {
                    JOptionPane.showMessageDialog(null, "Problemas ao excluir registro!");
                }
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecione um registro para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            String idString = String.valueOf(tblContrato.getValueAt(tblContrato.getSelectedRow(), 0));

            id = Integer.parseInt(idString);

            ContratoDAO apDAO = new ContratoDAO();

            Contrato ap = apDAO.consultarId(id);

            if (ap != null) {
                tfdId.setText(String.valueOf(ap.getId_contrato()));
                tfdIdSetor.setText(String.valueOf(ap.getId_setor()));
                tffSalAtual.setText(String.valueOf(ap.getSal_ant()));
                tffSalNovo.setText(String.valueOf(ap.getSal_novo()));
                tffDtUltimaAlt.setText(String.valueOf(ap.getDt_alteracao()));
                taDescricao.setText(ap.getObs());

                jTabbedPane1.setSelectedIndex(0);
            } else {
                System.out.println("erro na consulta");
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecione um registro para editar!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

